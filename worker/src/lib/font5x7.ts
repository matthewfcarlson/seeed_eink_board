/** Draws black text directly into an RGBA buffer, top-left anchored at (x0, y0). */
export function drawText5x7(
  rgba: Uint8ClampedArray,
  canvasWidth: number,
  canvasHeight: number,
  text: string,
  x0: number,
  y0: number,
  scale: number
): void {
  let cursorX = x0;
  for (const ch of text.toUpperCase()) {
    const glyph = FONT_5X7[ch] ?? FONT_5X7[" "]!;
    for (let row = 0; row < 7; row++) {
      const bits = glyph[row]!;
      for (let col = 0; col < 5; col++) {
        if (!((bits >> (4 - col)) & 1)) continue;
        for (let dy = 0; dy < scale; dy++) {
          const py = y0 + row * scale + dy;
          if (py < 0 || py >= canvasHeight) continue;
          for (let dx = 0; dx < scale; dx++) {
            const px = cursorX + col * scale + dx;
            if (px < 0 || px >= canvasWidth) continue;
            const o = (py * canvasWidth + px) * 4;
            rgba[o] = 0;
            rgba[o + 1] = 0;
            rgba[o + 2] = 0;
            rgba[o + 3] = 255;
          }
        }
      }
    }
    cursorX += 6 * scale; // 5 columns wide + 1 column of spacing
  }
}
